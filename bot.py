@bot.message_handler(commands=['start', 'help'])
def send_welcome(message):
    markup = InlineKeyboardMarkup()
    for subject in facts:
        markup.add(InlineKeyboardButton(subject, callback_data=subject))
    bot.send_message(message.chat.id, "Выберите предмет:", reply_markup=markup)

@bot.callback_query_handler(func=lambda call: call.data in facts)
def send_fact(call):
    subject = call.data
    fact = random.choice(facts[subject])
    markup = InlineKeyboardMarkup()
    markup.row(
        InlineKeyboardButton("Ещё факт", callback_data=subject),
        InlineKeyboardButton("Выбрать другой предмет", callback_data="menu")
    )
    bot.edit_message_text(
        f"{subject} — факт:\n{fact}",
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        reply_markup=markup
    )

@bot.callback_query_handler(func=lambda call: call.data == "menu")
def back_to_menu(call):
    markup = InlineKeyboardMarkup()
    for subject in facts:
        markup.add(InlineKeyboardButton(subject, callback_data=subject))
    bot.edit_message_text(
        "Выберите предмет:",
        chat_id=call.message.chat.id,
        message_id=call.message.message_id,
        reply_markup=markup
    )

bot.infinity_polling()
